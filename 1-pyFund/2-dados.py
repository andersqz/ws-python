
# python não precisa especificar o tipo da variável na declaração
# preferir usar underscore para variáveis com nomes compostos (padrão Py)
name = "Residente Evil 4 Remake"
year_launch = 2023
game_price = 300.00
plan_include = False

# printa as variáveis na tela
print(name)
print(year_launch)
print(game_price)
print(plan_include)

# função type() retorna o tipo de dado da variável
print(type(name))
print(type(year_launch))
print(type(game_price))
print(type(plan_include))