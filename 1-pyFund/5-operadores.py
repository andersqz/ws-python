
num1 = int(input("Digite o primeiro numero"))
num2 = int(input("Digite o segundo numero"))

# operadores aritmeticos

sum = num1 + num2
sub = num1 - num2
div = num1 / num2
mult = num1 * num2
mod = num1 % num2
exp = num1 ** num2

print(sum)
print(sub)
print(div)
print(mult)
print(mod)
print(exp)

print(f"resto da divisao de {num1} por {num2} eh {mod}")
print(f"sum of {num1} and {num2} is {sum}")


# operadores de comparação

bigger = num1 > num2
smaller = num1 < num2
equal = num1 == num2
different = num1 != num2
bigger_equal = num1 >= num2
smaller_equal = num1 <= num2

print(bigger)
print(smaller)
print(equal)
print(different)
print(bigger_equal)
print(smaller_equal)

print(f"Os numeros {num1} e {num2} sao iguais? {equal}")
print(f"O numero {num1} eh maior que {num2}? {bigger}")


